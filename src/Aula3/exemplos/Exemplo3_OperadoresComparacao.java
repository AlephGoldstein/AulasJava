package Aula3.exemplos;

public class Exemplo3_OperadoresComparacao{
    public static void main(String[] args){
        int num1 = 3, num2=7;
        boolean resposta;
        resposta = num1 > num2;
        System.out.println(resposta);
        resposta = num1 < num2;
        System.out.println(resposta);
        resposta = num1 == num2;
        System.out.println(resposta);
        resposta = num1 != num2;
        System.out.println(resposta);
        resposta = num1 <= num2;
        System.out.println(resposta);
        resposta = num1 >= num2;
        System.out.println(resposta);
    }
}

